export const fmtCarrier = (code: string | null | undefined): string => {
  if (!code) return ''
  const s = String(code).trim()
  if (!s) return ''
  if (CARRIER_MAP[s]) return CARRIER_MAP[s]
  // Some codes stored without prefix — try both catalog lookups
  if (/^\d+$/.test(s)) {
    if (CARRIER_MAP[`TRANS_MEX_${s}`]) return CARRIER_MAP[`TRANS_MEX_${s}`]
    if (CARRIER_MAP[`TRANS_EXT_${s}`]) return CARRIER_MAP[`TRANS_EXT_${s}`]
  }
  // Return raw value cleaned up — better than a generic fallback
  return s
    .replace(/^TRANS_MEX_/, '')
    .replace(/^TRANS_EXT_/, '')
    .replace(/_/g, ' ')
    .trim()
}

